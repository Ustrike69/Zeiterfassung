def t(key: str, lang: str | None = None) -> str:
    """Return translated string for key in current or given language."""
    if lang is None:
        try:
            from flask import session
            lang = session.get("lang", "de")
        except Exception:
            lang = "de"
    d = TRANSLATIONS.get(lang)
    if d and key in d:
        return d[key]
    # Fallback → 'en'
    en = TRANSLATIONS.get("en", {})
    if key in en:
        return en[key]
    # Fallback → 'de'
    de = TRANSLATIONS.get("de", {})
    return de.get(key, key)


def fmt_date(iso_date: str, lang: str | None = None) -> str:
    """Format ISO date string according to language convention."""
    if lang is None:
        try:
            from flask import session
            lang = session.get("lang", "de")
        except Exception:
            lang = "de"
    if not iso_date:
        return ""
    try:
        import datetime as _dt
        d = _dt.date.fromisoformat(str(iso_date)[:10])
        if lang == "en":
            return d.strftime("%m/%d/%Y")
        return d.strftime("%d.%m.%Y")
    except Exception:
        return str(iso_date)


def fmt_time(hhmm: str, lang: str | None = None) -> str:
    """Format HH:MM time string (24-hour in both languages for now)."""
    return str(hhmm or "")


def available_languages() -> list[tuple[str, str]]:
    """Return list of (code, label) for all supported languages."""
    return [("de", "Deutsch"), ("en", "English")]
